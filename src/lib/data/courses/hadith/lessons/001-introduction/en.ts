export const content = `# What is Hadith?

## Definition

Hadith (الحديث) refers to everything attributed to the Prophet ﷺ:
- Sayings (Qawl)
- Actions (Fi'l)
- Approvals (Taqrir)
- Physical and moral descriptions

## Structure of Hadith

1. **Isnad (السند)** - The chain of transmission
2. **Matn (المتن)** - The text of the hadith

## Importance of Hadith

Hadith is the second source of legislation after the Quran.`
