export const content = `# Introduction au Tajweed

## Qu'est-ce que le Tajweed ?

Le Tajweed (تجويد) vient de la racine arabe "j-w-d" qui signifie "améliorer" ou "rendre excellent". 

C'est la science de la récitation correcte du Coran, qui enseigne :
- La prononciation correcte de chaque lettre
- Les règles d'allongement (Madd)
- Les règles de nasalisation (Ghunna)
- Les règles d'arrêt (Waqf)

## Pourquoi apprendre le Tajweed ?

Allah dit dans le Coran :
> وَرَتِّلِ الْقُرْآنَ تَرْتِيلًا
> "Et récite le Coran lentement et clairement" (73:4)

Le Prophète ﷺ a dit : "Celui qui récite le Coran avec habileté sera avec les anges nobles et pieux."

## Les niveaux de récitation

1. **At-Tahqiq** : Récitation lente et mesurée
2. **Al-Hadr** : Récitation rapide
3. **At-Tadwir** : Récitation moyenne

Dans ce cours, nous nous concentrerons sur At-Tahqiq pour bien maîtriser les bases.`
