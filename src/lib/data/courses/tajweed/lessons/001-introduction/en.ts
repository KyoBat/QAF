export const content = `# Introduction to Tajweed

## What is Tajweed?

Tajweed (تجويد) comes from the Arabic root "j-w-d" meaning "to improve" or "to make excellent."

It is the science of correct Quran recitation, teaching:
- Correct pronunciation of each letter
- Rules of elongation (Madd)
- Rules of nasalization (Ghunna)
- Rules of stopping (Waqf)

## Why Learn Tajweed?

Allah says in the Quran:
> وَرَتِّلِ الْقُرْآنَ تَرْتِيلًا
> "And recite the Quran slowly and clearly" (73:4)

The Prophet ﷺ said: "The one who recites the Quran skillfully will be with the noble and righteous angels."

## Levels of Recitation

1. **At-Tahqiq**: Slow and measured recitation
2. **Al-Hadr**: Fast recitation
3. **At-Tadwir**: Medium-paced recitation`
