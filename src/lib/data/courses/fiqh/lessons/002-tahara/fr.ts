export const content = `# La Purification (Tahara)

## Importance de la Tahara

La purification est une condition essentielle pour la validité de la prière.

Le Prophète ﷺ a dit: "La clé de la prière est la purification."

## Types de purification

### 1. Al-Wudu (الوضوء) - Les ablutions mineures

**Actes obligatoires:**
- Laver le visage
- Laver les mains jusqu'aux coudes
- Essuyer la tête
- Laver les pieds jusqu'aux chevilles

### 2. Al-Ghusl (الغسل) - Les grandes ablutions

Obligatoire après:
- Les rapports conjugaux
- L'éjaculation
- Les menstrues
- Les lochies

### 3. At-Tayammum (التيمم) - Les ablutions sèches

Autorisé en cas de:
- Absence d'eau
- Maladie empêchant l'utilisation de l'eau`
