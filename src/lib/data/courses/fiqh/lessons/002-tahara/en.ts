export const content = `# Purification (Tahara)

## Importance of Tahara

Purification is an essential condition for the validity of prayer.

The Prophet ﷺ said: "The key to prayer is purification."

## Types of Purification

### 1. Al-Wudu (الوضوء) - Minor Ablution

**Obligatory acts:**
- Washing the face
- Washing hands up to elbows
- Wiping the head
- Washing feet up to ankles

### 2. Al-Ghusl (الغسل) - Major Ablution

Required after:
- Marital relations
- Ejaculation
- Menstruation
- Postpartum bleeding

### 3. At-Tayammum (التيمم) - Dry Ablution

Permitted when:
- Water is unavailable
- Illness prevents water use`
