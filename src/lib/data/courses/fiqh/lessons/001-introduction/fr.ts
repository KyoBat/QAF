export const content = `# Introduction au Fiqh

## Définition du Fiqh

Le Fiqh (الفقه) signifie littéralement "compréhension profonde". En terminologie islamique, c'est la science des règles pratiques de la Sharia déduites de leurs preuves détaillées.

## Sources du Fiqh

1. **Le Coran** - La parole d'Allah
2. **La Sunna** - Les enseignements du Prophète ﷺ
3. **L'Ijma** - Le consensus des savants
4. **Le Qiyas** - Le raisonnement par analogie

## Les quatre écoles juridiques

- **Hanafite** - Imam Abu Hanifa (699-767)
- **Malikite** - Imam Malik ibn Anas (711-795)
- **Shafiite** - Imam ash-Shafi'i (767-820)
- **Hanbalite** - Imam Ahmad ibn Hanbal (780-855)`
